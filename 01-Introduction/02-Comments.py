# This is a comment. Python ignores it.
# Comments are useful for explaining your code.

# You can write a comment like this:
# This line won't do anything when you run the code.

print("Comments don't show up when the code runs")  # This is an inline comment

# Python doesn't have multi-line comments like other languages,
# but you can use a triple-quoted string as a workaround:

"""
This is a multi-line comment
It's actually a multi-line string,
but Python will ignore it if it's not assigned to anything.
"""

print("Done with comments!")