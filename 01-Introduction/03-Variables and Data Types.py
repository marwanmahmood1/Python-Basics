# =========================
#        VARIABLES
# =========================

# Variables are labels we assign to values using the '=' sign.

# Example:
name = "Adam"
age = 30
height = 1.75
is_student = True

# You can print them:
print(name)
print(age)
print(height)
print(is_student)

# =========================
#       DATA TYPES
# =========================

# Python has several built-in data types. Here are the most common:

# Text, also named string (str)
greeting = "Hello"

# Numbers
age = 25            # This is an integer (int)
height = 1.70       # This is a float type of numerical data (float)

# Boolean (True or False)
is_happy = True

# None (this represents no value)
nothing = None

# You can check the type of any variable using 'type()'
print(type(greeting))  # <class 'str'>
print(type(age))       # <class 'int'>
print(type(is_happy))  # <class 'bool'>
