#     PYTHON VARIABLES
# =========================

# A variable is a name that refers to a value stored in memory.
# Use the equals sign (=) to assign a value to a variable.

# -------------------------------------
# 1. STRING (str): Textual data
name = "Adam"
print(name)

# 2. NUMERIC TYPES
age = 30              # Integer
height = 1.75         # Float
complex_num = 2 + 3j  # Complex number
print(age)
print(height)
print(complex_num)

# 3. BOOLEAN (bool): True or False values
is_student = True
print(is_student)

# 4. SEQUENCES
# - List: Mutable, ordered collection
fruits = ["apple", "banana", "cherry"]
print(fruits)

# - Tuple: Immutable, ordered collection
colors = ("red", "green", "blue")
print(colors)

# - Range: Immutable sequence of numbers
even_numbers = range(0, 10, 2)
print(list(even_numbers))  # Convert range to list for display
